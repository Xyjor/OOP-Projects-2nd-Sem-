    private void addBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBttnActionPerformed
        try {
            String name = txtName.getText();
            String category = txtCtgry.getText();
            double price = Double.parseDouble(txtPrc.getText());
            int quantity = Integer.parseInt(txtQntty.getText());

            if (name.isEmpty() || category.isEmpty() || txtPrc.getText().isEmpty() || txtQntty.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please fill all fields.");
                return;
            }

            Product p = new Product(category, name, price, quantity);
            ProductDAO dao = new ProductDAO();
            if (dao.insertProduct(p)) {
                JOptionPane.showMessageDialog(this, "Product added successfully!");
                loadProductTable();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add product.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Invalid input: " + e.getMessage());
        }
        txtCtgry.setText("");
        txtName.setText("");
        txtPrc.setText("");
        txtQntty.setText("");
        txtSrch.setText("");
        
    }//GEN-LAST:event_addBttnActionPerformed

    private void UpdtBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdtBttnActionPerformed
        int selectedRow = productsTbl.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a product to update.");
            return;
        }
        
        if (txtName.getText().isEmpty() || txtCtgry.getText().isEmpty() || txtPrc.getText().isEmpty() || txtQntty.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please fill all fields.");
                return;
            }

        try {
            int id = (int) productsTbl.getValueAt(selectedRow, 0);
            String name = txtName.getText();
            String category = txtCtgry.getText();
            double price = Double.parseDouble(txtPrc.getText());
            int quantity = Integer.parseInt(txtQntty.getText());

            Product p = new Product(id, name, category, price, quantity);
            ProductDAO dao = new ProductDAO();
            if (dao.updateProduct(p)) {
                JOptionPane.showMessageDialog(this, "Product updated successfully!");
                loadProductTable();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update product.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Invalid input: " + e.getMessage());
        }
        
        txtCtgry.setText("");
        txtName.setText("");
        txtPrc.setText("");
        txtQntty.setText("");
        txtSrch.setText("");

    }//GEN-LAST:event_UpdtBttnActionPerformed

    private void dltBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dltBttnActionPerformed
        int selectedRow = productsTbl.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a product to delete.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int id = (int) productsTbl.getValueAt(selectedRow, 0);
            ProductDAO dao = new ProductDAO();
            if (dao.deleteProduct(id)) {
                JOptionPane.showMessageDialog(this, "Product deleted successfully!");
                loadProductTable();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete product.");
            }
        }
        
        txtCtgry.setText("");
        txtName.setText("");
        txtPrc.setText("");
        txtQntty.setText("");
        txtSrch.setText("");

    }//GEN-LAST:event_dltBttnActionPerformed

    private void srchBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srchBttnActionPerformed
        String keyword = txtSrch.getText();
        ProductDAO dao = new ProductDAO();
        List<Product> results = dao.searchProducts(keyword);

        DefaultTableModel model = (DefaultTableModel) productsTbl.getModel();
        model.setRowCount(0);

        for (Product p : results) {
            model.addRow(new Object[]{
                p.getId(), p.getName(), p.getCategory(), p.getPrice(), p.getQuantity()
            });
        }
        
        txtCtgry.setText("");
        txtName.setText("");
        txtPrc.setText("");
        txtQntty.setText("");
        txtSrch.setText("");

    }//GEN-LAST:event_srchBttnActionPerformed

