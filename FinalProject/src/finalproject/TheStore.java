    private void loadProductTable() {
        DefaultTableModel model = (DefaultTableModel) productsTbl.getModel();
        model.setRowCount(0); // Clear the table

        ProductDAO dao = new ProductDAO();
        List<Product> productList = dao.getAllProducts();

        for (Product p : productList) {
            model.addRow(new Object[]{
                p.getId(),
                p.getCategory(),
                p.getName(),
                p.getPrice(),
                p.getQuantity()
            });
        }
    }

    private void addTableSelectionListener() {
        productsTbl.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int selectedRow = productsTbl.getSelectedRow();
                if (selectedRow >= 0) {
                    txtCtgry.setText(productsTbl.getValueAt(selectedRow, 1).toString());
                    txtName.setText(productsTbl.getValueAt(selectedRow, 2).toString());
                    txtPrc.setText(productsTbl.getValueAt(selectedRow, 3).toString());
                    txtQntty.setText(productsTbl.getValueAt(selectedRow, 4).toString());
                }
            }
        });
    }
    
    

