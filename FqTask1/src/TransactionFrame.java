    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
        int selectedRow = jtblProducts.getSelectedRow();

        if (selectedRow != -1) {
            DefaultTableModel ProductModel = (DefaultTableModel) jtblProducts.getModel();
            DefaultTableModel CartModel = (DefaultTableModel) jtblCart.getModel();

            String ProductName = ProductModel.getValueAt(selectedRow, 1).toString();
            double Price = Double.parseDouble(ProductModel.getValueAt(selectedRow, 4).toString());

            try {
                int Quantity = Integer.parseInt(txtfldQuantity.getText());

                if (Quantity <= 0) {
                    JOptionPane.showMessageDialog(this, "Quantity must be greater than 0");
                    return;
                }

                double TotalPrice = Price * Quantity;

                CartModel.addRow(new Object[]{ProductName, Price, Quantity, TotalPrice});
                UpdateOverallPrice();
                txtfldQuantity.setText("");

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Please input a valid quantity", "NUMBERS ONLY", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a product before adding to cart", "SELECT A PRODUCT FIRST", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAddToCartActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int selectedRow = jtblCart.getSelectedRow();

        if (selectedRow != -1) {
            DefaultTableModel cartModel = (DefaultTableModel) jtblCart.getModel();
            cartModel.removeRow(selectedRow);
            UpdateOverallPrice();
        } else {
            JOptionPane.showMessageDialog(this, "Please select a Product to remove.", "SELECT A PRODUCT", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnCheckoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckoutActionPerformed
        DefaultTableModel cartModel = (DefaultTableModel) jtblCart.getModel();
        if (cartModel.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Your cart is empty! Add items before proceeding to checkout!", "CART IS EMPTY", JOptionPane.ERROR_MESSAGE);
            return;
        }
        double checkoutPayment = UpdateOverallPrice();

        PaymentFrame checkout = new PaymentFrame(checkoutPayment);
        checkout.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnCheckoutActionPerformed

