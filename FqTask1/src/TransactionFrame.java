    public TransactionFrame(DefaultTableModel model) {
        initComponents();
        DefaultTableModel TransactionModel = (DefaultTableModel) jtblProducts.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            Object[] data = new Object[model.getColumnCount()];
            for (int j = 0; j < model.getColumnCount(); j++) {
                data[j] = model.getValueAt(i, j);
            }
            TransactionModel.addRow(data);
        }
        UpdateOverallPrice();

    }

    private double UpdateOverallPrice() {
        DefaultTableModel cartModel = (DefaultTableModel) jtblCart.getModel();
        double OverallTotal = 0;

        for (int i = 0; i < cartModel.getRowCount(); i++) {
            OverallTotal += Double.parseDouble(cartModel.getValueAt(i, 3).toString());
        }

        lblOverallTotal.setText("Overall Price: " + OverallTotal);

        return OverallTotal;
    }

